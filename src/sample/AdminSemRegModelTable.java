package sample;

public class AdminSemRegModelTable {
    String id,name,faculty,degree,year;

    public AdminSemRegModelTable(String id, String name, String faculty, String degree, String year) {
        this.id = id;
        this.name = name;
        this.faculty = faculty;
        this.degree = degree;
        this.year = year;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFaculty() {
        return faculty;
    }

    public void setFaculty(String faculty) {
        this.faculty = faculty;
    }

    public String getDegree() {
        return degree;
    }

    public void setDegree(String degree) {
        this.degree = degree;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }
}
