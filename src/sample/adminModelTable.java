package sample;

public class adminModelTable {
    String id,name,mail,role;

    public adminModelTable(String id, String name, String mail, String role) {
        this.id = id;
        this.name = name;
        this.mail = mail;
        this.role = role;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
